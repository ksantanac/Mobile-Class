// Função para calcular a média
fun main () {

    // Declarando as variaveis e atribuindo valores
    val nota1: Double = 10.0
    val nota2: Double = 5.5
    val nota3: Double = 8.7

    // calculando a média final
    val media: Double = (nota1 + nota2 + nota3) / 3

    // printando no terminal
    println("Sua média final é: $media")
}